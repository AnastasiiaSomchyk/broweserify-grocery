const event = require('./event');

const departmentOutputDiv = document.getElementById('departments');

const myPrintDepartments = (departments) => {
  let domStrang = '';
  departments.forEach((department) => {
    domStrang += `
    <div class="col-sm-3 departments">
      <img class="department-image" src="${department.img}">
      <h3 data-department-id="${department.id}" class='department-title hide'>${department.name}</h3>
    </div>`;
  });
  return domStrang;
};

const printToDom = (departments) => {
  departmentOutputDiv.innerHTML = myPrintDepartments(departments);
};

module.exports = printToDom;
